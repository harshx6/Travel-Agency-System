    private void getname(){
        try{
            String sql="select name from account where username=?";
            pst=conn.prepareStatement(sql);
            pst.setString(1,username);
            
            rs=pst.executeQuery();
            if(rs.next()){
                String name=rs.getString("Name");
                txtName.setText(name);
            }
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    private void calculateCost(){
        try{
            String sql;
            if (type.equals("Bus"))
            sql="select cost from bus_seats where bus_id=? and seatno=?";
            else{
                sql="select cost from air_seats where air_id=? and seatno=?";
            }
            pst=conn.prepareStatement(sql);
            pst.setString(1,busid);
            pst.setString(2,seat);
            rs=pst.executeQuery();
            if(rs.next()){
                String price=rs.getString("cost");
                txtCost.setText(price);
                
            }
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
