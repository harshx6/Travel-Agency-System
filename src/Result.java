    private void confirmbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmbuttonActionPerformed
        // TODO add your handling code here:
        try{
            String sql;
           
            
            ///inserting
            
            
            sql="Insert into booking(uname,type,item,company) values(?,?,?,?)";
           
            pst=conn.prepareStatement(sql);
            pst.setString(1,username);
            if (type.equals("Bus"))
            pst.setString(2,"Bus");
            else
            pst.setString(2,"Air");    
            pst.setString(3,seat);
            pst.setString(4,e_name);
            
            pst.execute();
           
            ///Deleting
            
            if (type.equals("Bus")){
                sql="Delete from bus_seats where bus_id=? and seatno=?";
            }
            else{
                sql="Delete from air_seats where air_id=? and seatno=?";
            }
            pst=conn.prepareStatement(sql);
            pst.setString(1,busid);
            pst.setString(2,seat);
            
            pst.execute();
            
            conn.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        setVisible(false);
        confirmt c=new confirmt(dest,username);
        c.setVisible(true);
    }//GEN-LAST:event_confirmbuttonActionPerformed

